class UserEntity {
  final String uid;
  final String fName;
  final String lName;
  final String userName;
  final String email;
  final String phoneNumber;
  final String imageurl;

  UserEntity({
    required this.uid,
    required this.fName,
    required this.lName,
    required this.userName,
    required this.email,
    required this.phoneNumber,
    required this.imageurl,
  });
}
